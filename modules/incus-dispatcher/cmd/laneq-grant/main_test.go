func TestRunMint_GeneratesVerifiableGrant(t *testing.T) {
	// SCENARIO-0117: mint produces a grant that verifies against the issuer public key.
	tmpdir := t.TempDir()
	issuerKeyPath := filepath.Join(tmpdir, "issuer.key")

	// Generate a client key and write its public PEM.
	clientKey, err := grantauth.NewKey()
	if err != nil {
		t.Fatalf("NewKey: %v", err)
	}
	clientPubPEM, err := clientKey.PublicKeyPEM()
	if err != nil {
		t.Fatalf("clientKey.PublicKeyPEM: %v", err)
	}
	clientPubPath := filepath.Join(tmpdir, "client.pub")
	if err := os.WriteFile(clientPubPath, []byte(clientPubPEM), 0644); err != nil {
		t.Fatalf("WriteFile client pub: %v", err)
	}

	// Run mint; should create issuer key and emit a grant.
	var stdout bytes.Buffer
	args := []string{
		"--sub", "agent-host",
		"--aud", "laneq://agent-host:9999",
		"--ttl", "30m",
		"--client-pub", clientPubPath,
		"--issuer-key", issuerKeyPath,
		"--kid", "k1",
		"--client-kid", "c1",
		"--iss", "mac",
	}

	err = runMint(args, &stdout)
	if err != nil {
		t.Fatalf("runMint: %v", err)
	}

	grant := strings.TrimSpace(stdout.String())
	if grant == "" {
		t.Fatalf("runMint produced empty grant")
	}

	// Load the issuer key that was generated.
	issuerKey, err := grantauth.LoadEd25519PrivateKeyPEM(issuerKeyPath)
	if err != nil {
		t.Fatalf("LoadEd25519PrivateKeyPEM: %v", err)
	}

	// Parse and verify the grant against the issuer's public key.
	tok, err := paseto.NewParserWithoutExpiryCheck().ParseV4Public(issuerKey.PublicKey(), grant, nil)
	if err != nil {
		t.Fatalf("ParseV4Public: %v", err)
	}

	// Verify claims are correct.
	var sub, aud, iss, jti string
	if err := tok.Get("sub", &sub); err != nil || sub != "agent-host" {
		t.Errorf("sub: got %q, want agent-host (err %v)", sub, err)
	}
	if err := tok.Get("aud", &aud); err != nil || aud != "laneq://agent-host:9999" {
		t.Errorf("aud: got %q, want laneq://agent-host:9999 (err %v)", aud, err)
	}
	if err := tok.Get("iss", &iss); err != nil || iss != "mac" {
		t.Errorf("iss: got %q, want mac (err %v)", iss, err)
	}
	if err := tok.Get("jti", &jti); err != nil || jti == "" {
		t.Errorf("jti: got %q, want non-empty (err %v)", jti, err)
	}

	// Verify cnf binds the client public key.
	var cnf struct {
		Kid string `json:"kid"`
		Key string `json:"key"`
	}
	if err := tok.Get("cnf", &cnf); err != nil {
		t.Fatalf("cnf: %v", err)
	}
	if cnf.Kid != "c1" {
		t.Errorf("cnf.kid: got %q, want c1", cnf.Kid)
	}
	if cnf.Key != clientPubPEM {
		t.Errorf("cnf.key: does not match client public key PEM")
	}

	// Verify expiry is set correctly (TTL = 30m from now).
	var exp int64
	if err := tok.Get("exp", &exp); err != nil {
		t.Fatalf("exp: %v", err)
	}
	// Check that exp is approximately 30 minutes from now (within 5 seconds).
	now := time.Now().Unix()
	expectedExp := now + 30*60
	if exp < expectedExp-5 || exp > expectedExp+5 {
		t.Errorf("exp: got %d, expected ~%d (±5s)", exp, expectedExp)
	}
}

func TestRunMint_IssuerKeyAutoGeneratedOnFirstRun(t *testing.T) {
	// SCENARIO-0117: issuer key is auto-generated at default path on first mint;
	// second mint reuses the same key.
	tmpdir := t.TempDir()
	issuerKeyPath := filepath.Join(tmpdir, "issuer.key")
	clientPubPath := filepath.Join(tmpdir, "client.pub")

	clientKey, err := grantauth.NewKey()
	if err != nil {
		t.Fatalf("NewKey: %v", err)
	}
	clientPubPEM, err := clientKey.PublicKeyPEM()
	if err != nil {
		t.Fatalf("clientKey.PublicKeyPEM: %v", err)
	}
	if err := os.WriteFile(clientPubPath, []byte(clientPubPEM), 0644); err != nil {
		t.Fatalf("WriteFile client pub: %v", err)
	}

	// First mint: key should be created.
	var stdout1 bytes.Buffer
	args1 := []string{
		"--sub", "agent-host",
		"--aud", "laneq://agent-host:9999",
		"--ttl", "30m",
		"--client-pub", clientPubPath,
		"--issuer-key", issuerKeyPath,
		"--kid", "k1",
		"--client-kid", "c1",
		"--iss", "mac",
	}
	err = runMint(args1, &stdout1)
	if err != nil {
		t.Fatalf("runMint 1: %v", err)
	}

	// Verify the key file exists with mode 0600.
	stat, err := os.Stat(issuerKeyPath)
	if err != nil {
		t.Fatalf("Stat issuer key: %v", err)
	}
	if (stat.Mode() & os.ModePerm) != 0600 {
		t.Errorf("issuer key mode: got %o, want 0600", stat.Mode()&os.ModePerm)
	}

	grant1 := strings.TrimSpace(stdout1.String())

	// Parse grant1 and extract issuer public key.
	issuerKey1, err := grantauth.LoadEd25519PrivateKeyPEM(issuerKeyPath)
	if err != nil {
		t.Fatalf("LoadEd25519PrivateKeyPEM: %v", err)
	}
	pubKey1, err := issuerKey1.PublicKeyPEM()
	if err != nil {
		t.Fatalf("PublicKeyPEM 1: %v", err)
	}

	// Second mint: should reuse the same key.
	var stdout2 bytes.Buffer
	err = runMint(args1, &stdout2)
	if err != nil {
		t.Fatalf("runMint 2: %v", err)
	}

	issuerKey2, err := grantauth.LoadEd25519PrivateKeyPEM(issuerKeyPath)
	if err != nil {
		t.Fatalf("LoadEd25519PrivateKeyPEM 2: %v", err)
	}
	pubKey2, err := issuerKey2.PublicKeyPEM()
	if err != nil {
		t.Fatalf("PublicKeyPEM 2: %v", err)
	}

	// Public keys should be identical.
	if pubKey1 != pubKey2 {
		t.Errorf("issuer key changed between runs (not reused)")
	}

	// Both grants should verify against the issuer key.
	grant2 := strings.TrimSpace(stdout2.String())
	for i, grant := range []string{grant1, grant2} {
		_, err := paseto.NewParserWithoutExpiryCheck().ParseV4Public(issuerKey1.PublicKey(), grant, nil)
		if err != nil {
			t.Errorf("grant %d verification: %v", i+1, err)
		}
	}
}

