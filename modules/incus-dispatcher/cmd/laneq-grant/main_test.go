func TestRunKeygen_GeneratesAndSavesKeys(t *testing.T) {
	tmpdir := t.TempDir()
	privPath := filepath.Join(tmpdir, "priv.pem")
	pubPath := filepath.Join(tmpdir, "pub.pem")

	var stdout bytes.Buffer
	args := []string{
		"--out-priv", privPath,
		"--out-pub", pubPath,
	}

	err := runKeygen(args, &stdout)
	if err != nil {
		t.Fatalf("runKeygen: %v", err)
	}

	// Verify private key file exists with mode 0600.
	privStat, err := os.Stat(privPath)
	if err != nil {
		t.Fatalf("Stat priv key: %v", err)
	}
	if (privStat.Mode() & os.ModePerm) != 0600 {
		t.Errorf("priv key mode: got %o, want 0600", privStat.Mode()&os.ModePerm)
	}

	// Verify public key file exists with mode 0644.
	pubStat, err := os.Stat(pubPath)
	if err != nil {
		t.Fatalf("Stat pub key: %v", err)
	}
	if (pubStat.Mode() & os.ModePerm) != 0644 {
		t.Errorf("pub key mode: got %o, want 0644", pubStat.Mode()&os.ModePerm)
	}

	// Verify private key can be loaded and round-trips.
	key, err := grantauth.LoadEd25519PrivateKeyPEM(privPath)
	if err != nil {
		t.Fatalf("LoadEd25519PrivateKeyPEM: %v", err)
	}

	// Verify public key is PKIX-parseable.
	pubData, err := os.ReadFile(pubPath)
	if err != nil {
		t.Fatalf("ReadFile pub key: %v", err)
	}
	if !strings.Contains(string(pubData), "-----BEGIN PUBLIC KEY-----") {
		t.Errorf("public key file not in PKIX PEM format")
	}

	// Verify the loaded key's public key matches the saved one.
	expectedPub, err := key.PublicKeyPEM()
	if err != nil {
		t.Fatalf("PublicKeyPEM: %v", err)
	}
	if string(pubData) != expectedPub {
		t.Errorf("saved public key does not match derived public key")
	}
}

